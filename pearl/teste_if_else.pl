print"DIGITE UM NUMERO:\n";
$sNumeroDigitado = getc();

if ($sNumeroDigitado == 0)
 {
  print("Zero");
 }
elsif ($sNumeroDigitado == 1)
 {
 print("Um"); 
 }
elsif ($sNumeroDigitado == 2)
 {
 print("Dois"); 
 }
elsif ($sNumeroDigitado == 3)
 {
 print("tres"); 
 }
elsif ($sNumeroDigitado == 4)
 {
 print("Quatro"); 
 }
elsif ($sNumeroDigitado == 5)
 {
 print("cinco"); 
 }
elsif ($sNumeroDigitado == 6)
 {
 print("Seis"); 
 }
elsif ($sNumeroDigitado == 7)
 {
 print("Sete");
 }
elsif ($sNumeroDigitado == 8)
 {
 print("Oito"); 
 }
elsif ($sNumeroDigitado == 9)
 {
 print("Nove");
 }
